commands = {
    (' ', 0): (' ', -1, 1),
    (' ', 2): (' ', 2, 2),
    (' ', 3): (' ', 2, 3),

    ('1', 1): ('0', -1, 2),
    ('1', 2): ('0', -1, 3),
    ('1', 3): ('1', -1, 2),

    ('0', 1): ('1', -1, 2),
    ('0', 2): ('1', -1, 3),
    ('0', 3): ('0', -1, 2),
}

def mt(s):
    s = list(' '+s+' ')
    q = 0
    i = len(s)-1
    while True:
        cmd = commands[(s[i], q)]
        s[i] = cmd[0]
        if cmd[1]==2: break
        i += cmd[1]
        q = cmd[2]
    return ''.join(s)

print(
    int(mt(
        f'{768:b}'
    ), 2)
)





